'''
小明身高1.75,体重80.5kg
BMI 体重除以身高的平方
低于18.5：过轻
大于18.5,小于等于25：正常
大于25,小于等于28：过重
大于28,小于等于32：肥胖
大于32：严重肥胖
'''
